@dataclass
class VCPPattern:
    """Represents a detected Volatility Contraction Pattern"""
    valid: bool
    pivot_price: float
    stop_loss: float
    contractions: List[Dict[str, float]]  # List of {depth: %, width: bars}
    base_high: float
    base_low: float
    volume_dry_up: bool
    tightness_score: float  # 0-100, higher is tighter
    
    def __repr__(self):
        if not self.valid:
            return "VCP(invalid)"
        return f"VCP(pivot=${self.pivot_price:.2f}, stop=${self.stop_loss:.2f}, tightness={self.tightness_score:.1f})"


@dataclass
class TrendTemplate:
    """Minervini's Trend Template criteria"""
    close_above_150sma: bool
    close_above_200sma: bool
    sma150_above_sma200: bool
    sma200_rising: bool
    within_25pct_of_52wh: bool
    rs_rating: float
    
    @property
    def all_passed(self) -> bool:
        """Check if all trend template criteria are met"""
        return (
            self.close_above_150sma and
            self.close_above_200sma and
            self.sma150_above_sma200 and
            self.sma200_rising and
            self.within_25pct_of_52wh and
            self.rs_rating >= 70  # Minervini prefers 80-90
        )
    
    def to_dict(self) -> Dict:
        return {
            "close_above_150sma": self.close_above_150sma,
            "close_above_200sma": self.close_above_200sma,
            "sma150_above_sma200": self.sma150_above_sma200,
            "sma200_rising": self.sma200_rising,
            "within_25pct_of_52wh": self.within_25pct_of_52wh,
            "rs_rating": self.rs_rating,
            "all_passed": self.all_passed
        }


